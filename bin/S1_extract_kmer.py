#!/usr/bin/env python3

from __future__ import annotations

import argparse
import sys
from pathlib import Path
from typing import Dict

from Bio.Seq import Seq


DMAP = {
    "a": "A",
    "t": "T",
    "g": "G",
    "c": "C",
    "A": "A",
    "T": "T",
    "G": "G",
    "C": "C",
}

CHECK_DICT = {
    "a": "A",
    "t": "T",
    "g": "G",
    "c": "C",
    "A": "A",
    "T": "T",
    "G": "G",
    "C": "C",
    "-": "",
}


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Extract SNP kmers from MSA and build VirStrain matrix files."
    )
    parser.add_argument(
        "-i",
        "--input_msa",
        required=True,
        help="Input MSA FASTA file",
    )
    parser.add_argument(
        "-k",
        "--kmer_size",
        type=int,
        default=25,
        help="K-mer size (default: 25)",
    )
    parser.add_argument(
        "-c",
        "--column_file",
        required=True,
        help="Column file generated by the preprocessing Perl script",
    )
    parser.add_argument(
        "-o",
        "--output_txt",
        default="Pos-snp-kmer-all.txt",
        help="Output kmer-to-pos_snp text file",
    )
    parser.add_argument(
        "-f",
        "--output_fasta",
        default="Pos-snp-kmer-all.fa",
        help="Output kmer FASTA file",
    )
    parser.add_argument(
        "--id-map-output",
        default="ID2Name.txt",
        help="Output ID to strain-name mapping file",
    )
    parser.add_argument(
        "--matrix-output",
        default="Strain_pos_snp_matrix_consider_all.txt",
        help="Output strain SNP matrix file",
    )
    return parser.parse_args()


def load_column_array(column_file: Path) -> list[int]:
    column_arr: list[int] = []
    seen: set[int] = set()

    with column_file.open("r", encoding="utf-8") as f:
        for raw_line in f:
            line = raw_line.strip()
            if not line or "column" not in line:
                continue
            fields = line.split()
            col = int(fields[1])
            if col not in seen:
                seen.add(col)
                column_arr.append(col)

    return sorted(column_arr)


def load_msa(msa_file: Path, id_map_output: Path) -> tuple[dict[str, str], dict[str, str], dict[int, str]]:
    dmsa: dict[str, str] = {}
    ds2id: dict[str, str] = {}
    did2s: dict[int, str] = {}

    current_name: str | None = None
    idx = 0

    with msa_file.open("r", encoding="utf-8") as f_in, id_map_output.open("w", encoding="utf-8") as f_out:
        for raw_line in f_in:
            line = raw_line.strip()
            if not line:
                continue

            if line.startswith(">"):
                current_name = line.split()[0].strip()
                dmsa[current_name] = ""
                f_out.write(f"{idx}\t{current_name}\n")
                ds2id[current_name] = str(idx)
                did2s[idx] = current_name
                idx += 1
            else:
                if current_name is None:
                    raise ValueError("Found sequence data before first FASTA header.")
                dmsa[current_name] += line

    return dmsa, ds2id, did2s


def build_kmer(left: str, base: str, right: str, k: int) -> str:
    half_k = (k - 1) // 2

    lseq = ""
    rseq = ""

    for l in left[::-1]:
        if len(lseq) == half_k:
            break
        if l in DMAP:
            lseq += DMAP[l]
        elif l != "-":
            lseq += l.upper()

    for r in right:
        if len(rseq) == half_k:
            break
        if r in DMAP:
            rseq += DMAP[r]
        elif r != "-":
            rseq += r.upper()

    lseq = lseq[::-1]
    kmr = lseq + base + rseq

    if len(kmr) < k:
        if len(lseq) < half_k:
            rseq = ""
            rl = k - 1 - len(lseq)
            for r in right:
                if len(rseq) == rl:
                    break
                if r in DMAP:
                    rseq += DMAP[r]
                elif r != "-":
                    rseq += r.upper()

        if len(rseq) < half_k:
            lseq = ""
            ll = k - 1 - len(rseq)
            for l in left[::-1]:
                if len(lseq) == ll:
                    break
                if l in DMAP:
                    lseq += DMAP[l]
                elif l != "-":
                    lseq += l.upper()
            lseq = lseq[::-1]

        kmr = lseq + base + rseq

    return kmr


def build_kmer_maps(
    dmsa: dict[str, str],
    ds2id: dict[str, str],
    column_arr: list[int],
    k: int,
) -> tuple[dict[int, dict[str, dict[str, int]]], dict[str, dict[str, str]], dict[str, dict[str, str]]]:
    dpbk: dict[int, dict[str, dict[str, int]]] = {}
    dkpb: dict[str, dict[str, str]] = {}
    dkl: dict[str, dict[str, str]] = {}

    total_column = len(column_arr)

    for i, c in enumerate(column_arr, start=1):
        print(f"Progress: {i}/{total_column}")

        for strain_name, seq in dmsa.items():
            if c >= len(seq):
                continue

            if seq[c] not in DMAP:
                continue

            base = DMAP[seq[c]]
            left = seq[:c]
            right = seq[c + 1 :]
            pos_base = f"{c}-{base}"

            left_block_start = max(0, c - 12)
            right_block_end = c + 13
            block_seq = seq[left_block_start:c] + base + seq[c + 1 : right_block_end]

            if any(b not in CHECK_DICT for b in block_seq):
                continue

            kmr = build_kmer(left, base, right, k)

            if c not in dpbk:
                dpbk[c] = {base: {kmr: 1}}
            else:
                if base not in dpbk[c]:
                    dpbk[c][base] = {kmr: 1}
                else:
                    dpbk[c][base][kmr] = 1

            if kmr not in dkl:
                dkl[kmr] = {ds2id[strain_name]: ""}
            else:
                dkl[kmr][ds2id[strain_name]] = ""

            if kmr not in dkpb:
                dkpb[kmr] = {pos_base: ""}
            else:
                dkpb[kmr][pos_base] = ""

    return dpbk, dkpb, dkl


def write_kmer_outputs(
    dkpb: dict[str, dict[str, str]],
    dkl: dict[str, dict[str, str]],
    output_txt: Path,
    output_fasta: Path,
) -> None:
    with output_txt.open("w", encoding="utf-8") as o_txt, output_fasta.open("w", encoding="utf-8") as o_fa:
        idx = 1
        for kmer in dkpb:
            info = ",".join(dkpb[kmer].keys())
            info2 = ",".join(dkl[kmer].keys())

            o_txt.write(f"{kmer}\t{info}\t{info2}\n")
            o_fa.write(f">{idx}\n{kmer}\n")
            idx += 1

            rc_kmer = str(Seq(kmer).reverse_complement())
            o_txt.write(f"{rc_kmer}\t{info}\t{info2}\n")
            o_fa.write(f">{idx}\n{rc_kmer}\n")
            idx += 1

    print("Kmer POS-Snp file done! Start generating matrix.")


def write_strain_matrix(
    dmsa: dict[str, str],
    column_arr: list[int],
    matrix_output: Path,
) -> None:
    base_arr = ["A", "T", "G", "C"]
    pos_snp: list[str] = []

    for c in column_arr:
        for b in base_arr:
            pos_snp.append(f"{c}-{b}")

    with matrix_output.open("w", encoding="utf-8") as o2:
        header = "\t".join(pos_snp)
        o2.write(f"\t\t{header}\n")

        for strain_name, seq in dmsa.items():
            temd: Dict[str, str] = {}
            for c in column_arr:
                if c >= len(seq):
                    temd[f"{c}-N"] = ""
                elif seq[c] not in DMAP:
                    temd[f"{c}-N"] = ""
                else:
                    temd[f"{c}-{DMAP[seq[c]]}"] = ""

            o2.write(strain_name)
            for p in pos_snp:
                if p not in temd:
                    o2.write("\t0")
                else:
                    o2.write("\t1")
            o2.write("\n")


def main() -> int:
    args = parse_args()

    input_msa = Path(args.input_msa).resolve()
    cfile = Path(args.column_file).resolve()
    output_txt = Path(args.output_txt).resolve()
    output_fasta = Path(args.output_fasta).resolve()
    id_map_output = Path(args.id_map_output).resolve()
    matrix_output = Path(args.matrix_output).resolve()
    k = args.kmer_size

    if k < 1 or k % 2 == 0:
        raise ValueError("k-mer size must be a positive odd integer.")

    column_arr = load_column_array(cfile)
    dmsa, ds2id, _did2s = load_msa(input_msa, id_map_output)

    _dpbk, dkpb, dkl = build_kmer_maps(dmsa, ds2id, column_arr, k)
    write_kmer_outputs(dkpb, dkl, output_txt, output_fasta)
    write_strain_matrix(dmsa, column_arr, matrix_output)

    return 0


if __name__ == "__main__":
    sys.exit(main())
